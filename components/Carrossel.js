////////////////////////////////////////////////////////////////////////////////////////////////////
// Componente Carrossel
////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////
// Importações
////////////////////////////////////////////////////////////////////////////////////////////////////
import React from 'react'; // React
import { View, Image, Dimensions } from 'react-native'; // Elementos nativos do React Native
import Carousel from 'react-native-snap-carousel'; // Carrossel
import tailwind from 'tailwind-rn'; // Estilos

////////////////////////////////////////////////////////////////////////////////////////////////////
// Dados
////////////////////////////////////////////////////////////////////////////////////////////////////
const data = [ // Dados de exemplo
  { uri: 'https://via.placeholder.com/400x300' },
  { uri: 'https://via.placeholder.com/400x300' },
  { uri: 'https://via.placeholder.com/400x300' },
];

////////////////////////////////////////////////////////////////////////////////////////////////////
// Componente
////////////////////////////////////////////////////////////////////////////////////////////////////
const Carrossel = () => { // Componente Carrossel
  const screenWidth = Dimensions.get('window').width; // Largura da tela

  const renderItem = ({ item }) => ( // Renderização de cada item
    <View style={tailwind('flex-1 justify-center items-center')}> {/* Estilo do item */}
      <Image source={{ uri: item.uri }} style={{ width: screenWidth - 60, height: 200 }} /> {/* Imagem */}
    </View> // Fim do item
  );

  // Retorno do componente
  return (
    <Carousel // Carrossel
      data={data} // Dados
      renderItem={renderItem} // Função de renderização
      sliderWidth={screenWidth} // Largura do slider
      itemWidth={screenWidth - 60} // Largura do item
      loop={true} // Loop infinito
    />
  );
};

export default Carrossel; // Exporta o componente Carrossel

